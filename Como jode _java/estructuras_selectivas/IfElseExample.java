package estructuras_selectivas;
//Ejemplo de if-else
public class IfElseExample {
    public static void main(String[] args) {
        int numero = 10;
        if (numero > 0) {
            System.out.println("El número es positivo"); // Se ejecuta si la condición es verdadera
        } else {
            System.out.println("El número es negativo o cero"); // Se ejecuta si la condición es falsa
        }
    }
}
