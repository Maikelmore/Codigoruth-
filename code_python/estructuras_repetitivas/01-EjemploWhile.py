# Ejemplo bucle while
# El bucle se ejecuta mientras la condición sea true

# Ejemplo imprimir números del 1 al 5

contador =6
while contador<=5:
    print(contador)
    contador+=1 # En Python no tenemos el contador++ q usamos en Java. Aquí usamos +=
print("Fin programa. Adios")    


